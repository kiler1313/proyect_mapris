package com.mapris.modelo.entities;

import java.io.Serializable;
import java.util.List;
import javax.persistence.Basic;
import javax.persistence.Column;
import javax.persistence.Entity;
import javax.persistence.FetchType;
import javax.persistence.Id;
import javax.persistence.JoinColumn;
import javax.persistence.JoinTable;
import javax.persistence.Lob;
import javax.persistence.ManyToMany;
import javax.persistence.NamedQueries;
import javax.persistence.NamedQuery;
import javax.persistence.Table;
import javax.xml.bind.annotation.XmlRootElement;
import javax.xml.bind.annotation.XmlTransient;

/**
 *
 * @author Ruben
 */
@Entity
@Table(name = "roles")
@XmlRootElement
@NamedQueries({
    @NamedQuery(name = "Rol.findAll", query = "SELECT r FROM Rol r")
    , @NamedQuery(name = "Rol.findByIdRoles", query = "SELECT r FROM Rol r WHERE r.idRoles = :idRoles")
    , @NamedQuery(name = "Rol.findByNombre", query = "SELECT r FROM Rol r WHERE r.nombre = :nombre")})
public class Rol implements Serializable {

    private static final long serialVersionUID = 1L;
    @Id
    @Basic(optional = false)
   
    @Column(name = "idRoles")
    private Integer idRoles;
    @Basic(optional = false)
   
    @Column(name = "nombre")
    private String nombre;
    @Lob
   
    @Column(name = "descripcion")
    private String descripcion;
    @ManyToMany(mappedBy = "rolList", fetch = FetchType.EAGER)
    private List<Permiso> permisoList;
    @JoinTable(name = "rolesusuarios", joinColumns = {
        @JoinColumn(name = "idRoles", referencedColumnName = "idRoles")}, inverseJoinColumns = {
        @JoinColumn(name = "idUsuarios", referencedColumnName = "cedula")})
    @ManyToMany(fetch = FetchType.EAGER)
    private List<Usuario> usuarioList;

    public Rol() {
    }

    public Rol(Integer idRoles) {
        this.idRoles = idRoles;
    }

    public Rol(Integer idRoles, String nombre) {
        this.idRoles = idRoles;
        this.nombre = nombre;
    }

    public Integer getIdRoles() {
        return idRoles;
    }

    public void setIdRoles(Integer idRoles) {
        this.idRoles = idRoles;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public String getDescripcion() {
        return descripcion;
    }

    public void setDescripcion(String descripcion) {
        this.descripcion = descripcion;
    }

    @XmlTransient
    public List<Permiso> getPermisoList() {
        return permisoList;
    }

    public void setPermisoList(List<Permiso> permisoList) {
        this.permisoList = permisoList;
    }

    @XmlTransient
    public List<Usuario> getUsuarioList() {
        return usuarioList;
    }

    public void setUsuarioList(List<Usuario> usuarioList) {
        this.usuarioList = usuarioList;
    }

    @Override
    public int hashCode() {
        int hash = 0;
        hash += (idRoles != null ? idRoles.hashCode() : 0);
        return hash;
    }

    @Override
    public boolean equals(Object object) {
        // TODO: Warning - this method won't work in the case the id fields are not set
        if (!(object instanceof Rol)) {
            return false;
        }
        Rol other = (Rol) object;
        if ((this.idRoles == null && other.idRoles != null) || (this.idRoles != null && !this.idRoles.equals(other.idRoles))) {
            return false;
        }
        return true;
    }

    @Override
    public String toString() {
        return "Rol{" + "idRoles=" + idRoles + ", nombre=" + nombre + ", descripcion=" + descripcion + '}';
    }

   
    
}
